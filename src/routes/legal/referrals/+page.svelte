	<h1>Referral programme terms</h1>
	<p>
		Future of Work freelancers can invite friends with a personal referral code. When that friend
		signs up using the code, verifies their email, and makes their first non-spam submission, the
		referrer earns one submission credit. Each time the referred friend wins a bounty, the referrer
		earns a bonus credit set by the platform.
	</p>
	<h2>Eligibility</h2>
	<ul>
		<li>You must have a freelancer profile to participate.</li>
		<li>Referred friends must verify their email before any credit is granted.</li>
		<li>Self-referrals and duplicate accounts (same email or phone) do not count.</li>
		<li>Each user has a global referral cap displayed on their dashboard.</li>
	</ul>
	<h2>Spam and abuse</h2>
	<p>
		If a submission that earned you a referral credit is later labelled SPAM by a moderator, the
		credit is reversed. Repeated abuse can result in the platform revoking referral access.
	</p>
	<h2>Changes</h2>
	<p>
		Future of Work may adjust the cap, credit amounts, or terms of the programme at any time. Earned
		credits already in your balance are not affected by future changes.
	</p>
</div>
